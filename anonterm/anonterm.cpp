void typer(string text, int delay){
    for (char c : text){
        cout << c << flush;
        this_thread::sleep_for(chrono::microseconds(delay));
    }
}

void help(){
  string menu = R"(
   ___________________________________
  |     COMMAND    |      PURPOSE     |
  |................|..................|
  |clear                clear terminal|
  |-----------------------------------|
  |ifconfig        display wifi interf|
  |-----------------------------------|
  |ip a            display wifi interf|
  |-----------------------------------|
  |exit                exit the script|
  |-----------------------------------|
  |clear logs      clears the log file|
  |-----------------------------------|
  )";
 typer(menu, 1000); 
}

void log(string data){
    ofstream logfile("logs.txt", ios::app);
    time_t currentTime = std::time(nullptr);
    tm* localTime = std::localtime(&currentTime);
    ostringstream oss;
    oss << '['<<(localTime->tm_year + 1900) << '/'           // Year
        << (localTime->tm_mon + 1) << '/'              // Month
        << localTime->tm_mday << ' '                   // Day
        << localTime->tm_hour << ':'                  // Hour
        << localTime->tm_min << ':'                   // Minute
        << localTime->tm_sec << "]  ";                        // Second

    if(logfile.is_open()){
        logfile << oss.str() << data.c_str() << endl;
    }
}

void executer(string command){
    list<string> excluded = {"ifconfig", "clear", "ip a", "clear logs"};
    auto it = find(excluded.begin(), excluded.end(), command);
    if (it != excluded.end()){
        if(command == "clear logs"){command = "rm -r logs.txt";}
        log(command);
        system(command.c_str());
    }
    else if(command == "help"){
        help();
    }
    else{
        string anon_command = "proxychains " + command;
        log(anon_command);
        system(anon_command.c_str());
    }
}
